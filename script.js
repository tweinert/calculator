function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log("add: " + add(6, 4));
console.log("subtract: " + subtract(16, 6));
console.log("multiply: " + multiply(5, 8));
console.log("divide: " + divide(64, 8));
